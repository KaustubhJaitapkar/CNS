RCON = (
    0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
)


def mul(a, b):

    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        a <<= 1
        if a & 0x100:  
            a ^= 0x11b 
        b >>= 1
    return p % 256

def sub_word(word):
    return [S_BOX[b] for b in word]

def rot_word(word):
    return word[1:] + word[:1]

def state_from_block(block):
    state = [[0] * 4 for _ in range(4)]
    for i in range(4):
        for j in range(4):
            state[i][j] = block[i + 4 * j]
    return state

def block_from_state(state):
    block = [0] * 16
    for i in range(4):
        for j in range(4):
            block[i + 4 * j] = state[i][j]
    return block


def key_expansion(key):

    key_words = [key[i:i+4] for i in range(0, 16, 4)]
    expanded_key = copy.deepcopy(key_words)

    i = 4  
    while len(expanded_key) < 44:
        temp = expanded_key[i - 1]

        if i % 4 == 0:
            temp = sub_word(rot_word(temp))
            temp[0] ^= RCON[i // 4 - 1]

        word_a = expanded_key[i - 4]
        new_word = [temp[j] ^ word_a[j] for j in range(4)]
        expanded_key.append(new_word)
        i += 1
        
    return expanded_key


def sub_bytes(state):
    for i in range(4):
        for j in range(4):
            state[i][j] = S_BOX[state[i][j]]

def shift_rows(state):
    state[1] = state[1][1:] + state[1][:1]
    state[2] = state[2][2:] + state[2][:2]
    state[3] = state[3][3:] + state[3][:3]

def mix_columns(state):
    
    for j in range(4):
        s0, s1, s2, s3 = state[0][j], state[1][j], state[2][j], state[3][j]
        
        def xtime(a):
            return (a << 1) ^ 0x11b if a & 0x80 else (a << 1)
        

        state[0][j] = xtime(s0) ^ mul(0x03, s1) ^ s2 ^ s3
        state[1][j] = s0 ^ xtime(s1) ^ mul(0x03, s2) ^ s3
        state[2][j] = s0 ^ s1 ^ xtime(s2) ^ mul(0x03, s3)
        state[3][j] = mul(0x03, s0) ^ s1 ^ s2 ^ xtime(s3)

def add_round_key(state, round_key_words):
    """XORs the state with the current round key."""
    for j in range(4): 
        key_word = round_key_words[j]
        for i in range(4): 
            state[i][j] ^= key_word[i]
            

def aes_encrypt_block(plaintext_block, key):

    if len(plaintext_block) != 16 or len(key) != 16:
        raise ValueError("Block and key must be exactly 16 bytes (128 bits) long.")
        
    state = state_from_block(plaintext_block)
    expanded_key = key_expansion(key)
    
    add_round_key(state, expanded_key[0:4])
    
    for r in range(1, 10):
        sub_bytes(state)
        shift_rows(state)
        mix_columns(state)
        add_round_key(state, expanded_key[4 * r : 4 * r + 4])

    sub_bytes(state)
    shift_rows(state)
    add_round_key(state, expanded_key[40:44]) 
    
    return block_from_state(state)


